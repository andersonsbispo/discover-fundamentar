// Operadores Aritiméticos

// multiplicação *
console.log(3 * 5);

// divisão /
console.log(12 / 2);

// soma +
console.log(34 + 57);

// subtração -
console.log(34 - 23);

// resto da divisão %
let remainder;
remainder = 11 % 9;
console.log(remainder);

// incremento ++
let increment = 0;
increment++;
console.log(increment);

// decremento --
let decrement = 0;
decrement--;
console.log(decrement);

// exponencial **
console.log(2 ** 3);